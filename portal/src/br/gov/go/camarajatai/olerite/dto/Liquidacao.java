package br.gov.go.camarajatai.olerite.dto;

public class Liquidacao {

	private String mensagem;
	private String descricaoAfastamento;
	private String valorBruto;
	private String valorLiquido;
	private String totalDesconto;
	
	public String getMensagem() {
		return mensagem;
	}
	public void setMensagem(String mensagem) {
		this.mensagem = mensagem;
	}
	public String getDescricaoAfastamento() {
		return descricaoAfastamento;
	}
	public void setDescricaoAfastamento(String descricaoAfastamento) {
		this.descricaoAfastamento = descricaoAfastamento;
	}
	public String getValorBruto() {
		return valorBruto;
	}
	public void setValorBruto(String valorBruto) {
		this.valorBruto = valorBruto;
	}
	public String getValorLiquido() {
		return valorLiquido;
	}
	public void setValorLiquido(String valorLiquido) {
		this.valorLiquido = valorLiquido;
	}
	public String getTotalDesconto() {
		return totalDesconto;
	}
	public void setTotalDesconto(String totalDesconto) {
		this.totalDesconto = totalDesconto;
	}
	
	
	
	
	
	
}
